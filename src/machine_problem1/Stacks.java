    private void Btn_PopMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_PopMouseClicked
        if (exist){
            try {
                int value_pop = DerivedStacks.popData();
                
                if (value_pop != -1){
                    Txt_Area.setText(Txt_Area.getText() + "• Popped the value of " + value_pop);
                } else {
                    Txt_Area.setText(Txt_Area.getText() + "• STACKUNDERFLOW");
                }
                Txt_Area.setText(Txt_Area.getText() + "\n\n");
                
              } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Pop element - Stack", JOptionPane.INFORMATION_MESSAGE);
              }
        } else {
            JOptionPane.showMessageDialog(null, "Please Create & insert values first on the Stack", "Pop element - Stack", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Btn_PopMouseClicked

    private void Btn_ChangeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_ChangeMouseClicked
        if (exist){
            try {
                String userInput = JOptionPane.showInputDialog(null, "Please enter the index to be replaced" , "Change element - Stack", JOptionPane.INFORMATION_MESSAGE);
                int int_Input = Integer.parseInt(userInput);
                
                String userValue = JOptionPane.showInputDialog(null, "Please enter the new value of the index" , "Change element - Stack", JOptionPane.INFORMATION_MESSAGE);
                int int_Value = Integer.parseInt(userValue);
                
                int value_change = DerivedStacks.changeElementData(int_Input, int_Value);
                
                if (value_change != 2){
                    Txt_Area.setText(Txt_Area.getText() + "• Change the value of element " + int_Input + " to " + int_Value);
                } else {
                    Txt_Area.setText(Txt_Area.getText() + "• Index out of range");
                }
                Txt_Area.setText(Txt_Area.getText() + "\n\n");
                
              } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Change element - Stack", JOptionPane.INFORMATION_MESSAGE);
              }
        } else {
            JOptionPane.showMessageDialog(null, "Please Create & insert values first on the Stack", "Change element - Stack", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Btn_ChangeMouseClicked


