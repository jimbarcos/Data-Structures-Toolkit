    private void Btn_CreateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_CreateMouseClicked

        if (exist){
            if (JOptionPane.showConfirmDialog(null, "Create A new empty Stack (This will overwrite the previous Stack)", "Create Stack", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
               exist = false;
            }
        }
        
        if (exist == false) {
            try {
                String userInput = JOptionPane.showInputDialog(null, "Please enter how many storage for stack:" , "Create element - Stack", JOptionPane.INFORMATION_MESSAGE);
                int int_Input = Integer.parseInt(userInput);
                DerivedStacks = new StackAlgo(int_Input);
                Txt_Area.setText(Txt_Area.getText() + "• Successfully Completed Creating An empty Stack with " + int_Input + " size\n\n");
                JOptionPane.showMessageDialog(null, "Successfully Completed Creating An empty Stack with " + int_Input + " size", "Create Stack", JOptionPane.INFORMATION_MESSAGE);
                exist = true;
                num_counter = 1;
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Create element - Stack", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_Btn_CreateMouseClicked

    private void Btn_PushMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_PushMouseClicked
         try{
            if (exist){
            String userInput = JOptionPane.showInputDialog(null, "Please enter the element #" + (num_counter), "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
            try {
                int int_Input = Integer.parseInt(userInput);
                
                if (DerivedStacks.pushData(int_Input) == 2){
                    num_counter++;
                    Txt_Area.setText(Txt_Area.getText() + "• Value " + int_Input + " inserted in the Stack\n\n");
                }else {
                    Txt_Area.setText(Txt_Area.getText() + "• STACKOVERFLOW\n\n");
                }
                
                
                
              } catch (Exception e) {
                // Handle the exception if the user input cannot be converted to an integer
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
              }
            
            } else {
                JOptionPane.showMessageDialog(null, "Please Create first an empty Stack", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Btn_PushMouseClicked

