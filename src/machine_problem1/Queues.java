    private void Btn_EmptyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_EmptyMouseClicked
        if (exist){
            if (JOptionPane.showConfirmDialog(null, "Create A new empty Queue (This will overwrite the previous Queue)", "Create Queue", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION){
               exist = false;
            }
        }
        
        if (exist == false) {
            try {
                String userInput = JOptionPane.showInputDialog(null, "Please enter how many storage for Queue:" , "Create element - Queue", JOptionPane.INFORMATION_MESSAGE);
                int int_Input = Integer.parseInt(userInput);
                DerivedQueue = new QueuesAlgo(int_Input);
                Txt_Area.setText(Txt_Area.getText() + "‚Ä¢ Successfully Completed Creating An empty Queue with " + int_Input + " size\n\n");
                JOptionPane.showMessageDialog(null, "Successfully Completed Creating An empty Queue with " + int_Input + " size", "Create Queue", JOptionPane.INFORMATION_MESSAGE);
                exist = true;
                num_counter = 1;
            } catch (Exception e){
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_Btn_EmptyMouseClicked

    private void Btn_EnqueueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_EnqueueMouseClicked
        try{
            if (exist){
            String userInput = JOptionPane.showInputDialog(null, "Please enter the element #" + (num_counter), "Push element - Queue", JOptionPane.INFORMATION_MESSAGE);
            try {
                int int_Input = Integer.parseInt(userInput);
                
                if (DerivedQueue.enqueueData(int_Input) == 2){
                    num_counter++;
                    Txt_Area.setText(Txt_Area.getText() + "‚Ä¢ Value " + int_Input + " inserted in the Queue\n\n");
                }else {
                    Txt_Area.setText(Txt_Area.getText() + "‚Ä¢ QUEUEOVERFLOW\n\n");
                }
              } catch (Exception e) {
                // Handle the exception if the user input cannot be converted to an integer
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Push element - Queue", JOptionPane.INFORMATION_MESSAGE);
              }
            
            } else {
                JOptionPane.showMessageDialog(null, "Please Create first an empty Queue", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Push element - Stack", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Btn_EnqueueMouseClicked

