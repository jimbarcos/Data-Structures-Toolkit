    private void Btn_DequeueMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_DequeueMouseClicked
        if (exist){
            try {
                int value_pop = DerivedQueue.dequeueData();
                
                if (value_pop != -1){
                    Txt_Area.setText(Txt_Area.getText() + "‚Ä¢ Dequeue the value of " + value_pop);
                } else {
                    Txt_Area.setText(Txt_Area.getText() + "‚Ä¢ QUEUEUNDERFLOW");
                }
                Txt_Area.setText(Txt_Area.getText() + "\n\n");
                
              } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Dequeue element - Dequeue", JOptionPane.INFORMATION_MESSAGE);
              }
        } else {
            JOptionPane.showMessageDialog(null, "Please Create & insert values first on the Queue", "Dequeue element - Dequeue", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_Btn_DequeueMouseClicked

